// ── Agent Logs ──────────────────────────────────────────────────────────
function makeLogs(scanId: string, agents: string[], baseMinutes: number): AgentLog[] {
  const levels = ["info", "info", "info", "info", "warning"];
  const messages: Record<string, string[]> = {
    recon: [
      "Starting reconnaissance on target",
      "DNS enumeration found 12 subdomains",
      "Port scan: 80, 443, 8080 open",
      "Technology fingerprint: Node.js, Express, Angular",
      "Collected 47 URLs from sitemap and crawl",
    ],
    scanner: [
      "Initializing vulnerability scanner",
      "Testing for XSS on 47 endpoints",
      "Testing for SQL injection on 23 parameters",
      "Testing for CSRF on 8 forms",
      "Found 3 potential vulnerabilities",
    ],
    exploit: [
      "Starting exploit verification",
      "Verifying XSS on /rest/products/search — CONFIRMED",
      "Verifying SQLi on /rest/user/login — CONFIRMED",
      "Verification complete: 2/3 confirmed",
    ],
    analyzer: [
      "Running AI-powered analysis",
      "Calculating CVSS scores for 7 findings",
      "Mapping CWE identifiers",
      "Generating fix recommendations",
      "Analysis complete — 1 critical, 2 high, 2 medium, 1 low, 1 info",
    ],
    reporter: [
      "Generating PDF report",
      "Building executive summary",
      "Adding detailed findings section",
      "Report generated successfully (24 pages)",
    ],
  };
  const logs: AgentLog[] = [];
  let minOffset = 0;
  for (const agent of agents) {
    const msgs = messages[agent] || ["Processing..."];
    for (let j = 0; j < msgs.length; j++) {
      logs.push({
        id: uid(400 + logs.length),
        scan_id: scanId,
        agent_name: agent,
        level: levels[j % levels.length],
        message: msgs[j],
        data: null,
        timestamp: agoMin(baseMinutes - minOffset),
      });
      minOffset += 1;
    }
  }
  return logs;
}

