// ── Finding Summaries ───────────────────────────────────────────────────
function buildSummary(findings: Finding[]): FindingSummary {
  const s: FindingSummary = { total: findings.length, critical: 0, high: 0, medium: 0, low: 0, info: 0, confirmed: 0, false_positives: 0 };
  for (const f of findings) {
    s[f.severity]++;
    if (f.confirmed) s.confirmed++;
    if (f.false_positive) s.false_positives++;
  }
  return s;
}

